// Функция для получения статьи по ID
export const getNewsArticleById = (id: string): NewsArticle | null => {
  return newsArticles.find(article => article.id === id) || null
}

// Функция для получения похожих новостей
export const getRelatedNews = (currentId: string, category: string, limit: number = 3): NewsArticle[] => {
  return newsArticles
    .filter(article => article.category === category && article.id !== currentId)
    .slice(0, limit)
}

// Функция для получения рекомендуемых новостей
export const getFeaturedNews = (limit: number = 3): NewsArticle[] => {
  return newsArticles
    .filter(article => article.featured)
    .slice(0, limit)
}
